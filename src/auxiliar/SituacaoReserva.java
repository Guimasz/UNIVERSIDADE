/**
 * A enumeração `SituacaoReserva` representa os possíveis estados de uma reserva no sistema de reserva da Ucsal.
 * Pode ser APROVADA ou REPROVADA.
 *
 * @author BRUNO CASÉ and ZAIRA DUTRA
 * @version 1.0
 * @since 21/11/2023
 */
package auxiliar;

/**
 * Enumeração que define os possíveis estados de uma reserva.
 */
enum SituacaoReserva {

    /**
     * Indica que a reserva foi aprovada.
     */
    APROVADA,

    /**
     * Indica que a reserva foi reprovada.
     */
    REPROVADA
}
